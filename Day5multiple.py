
##MULTIPLE INHERITANCE ##
class student:



    name="aashish kumar saini"
    rollno="22eldcs003"
    def show(self):
        print("name  is ",self.name)
        print("roll no is",self.rollno)
class test:
    mark1=99
    mark2=98
    def disp(self):
        print("marks in subject 1",self.mark1)
        print("marks in subject 2",self.mark2)
class result(student,test):  #INHERITING MULTIPLE CLASS IN A DERIVED CLASS
    
    def total(self):
        self.t=self.mark1+self.mark2
    def display(self):
        self.show()
        self.disp()
        self.total()
        print("total marks is",self.t)

a=result()
a.display()